DROP DATABASE IF EXISTS css475_NBA;
CREATE DATABASE css475_NBA;
USE css475_NBA;

DROP TABLE IF EXISTS TEAM;
CREATE TABLE TEAM
(
	TeamId				INT unsigned PRIMARY KEY auto_increment NOT NULL,
    TeamName			VARCHAR(40) NOT NULL,
    CoachId				INT unsigned NOT NULL,
    ArenaId				INT unsigned NOT NULL,
    Conference			ENUM('West', 'East') NOT NULL,
    Division			ENUM('Pacific', 'Northwest', 'Southwest', 'Atlantic', 'Central', 'Southeast'),
	Championships		INT NULL,
    Wins				INT NOT NULL,
    Losses				INT NOT NULL
);

DROP TABLE IF EXISTS PLAYER;
CREATE TABLE PLAYER
(
	PlayerId			INT unsigned NOT NULL PRIMARY KEY,
    TeamId				INT unsigned NOT NULL,
    Fname				VARCHAR(40) NOT NULL,
    Lname				VARCHAR(40),
    DOB					DATE NOT NULL,
    Height_ft			INT unsigned NOT NULL,
    Height_inch			INT unsigned NOT NULL,
    Weight				INT unsigned NOT NULL,
    DraftYear			YEAR NULL
);

DROP TABLE IF EXISTS ARENA;
CREATE TABLE ARENA
(
	ArenaId			INT unsigned NOT NULL PRIMARY KEY auto_increment,
    Name			VARCHAR(50) NOT NULL,
    Address			VARCHAR(80) NOT NULL,
    City			VARCHAR(40) NOT NULL,
    State			VARCHAR(2) NOT NULL,
    Capacity		INT unsigned NOT NULL,
    BuiltIn			INT NOT NULL
);

DROP TABLE IF EXISTS HEAD_COACH;
CREATE TABLE HEAD_COACH
(
	CoachId		INT unsigned NOT NULL PRIMARY KEY,
    Name		VARCHAR(40) NOT NULL,
    Wins		INT NOT NULL,
    Losses		INT NOT NULL,
    DOB			DATE NOT NULL
);

DROP TABLE IF EXISTS PLAYED_FOR;
CREATE TABLE PLAYED_FOR
(
	PlayerId		INT unsigned NOT NULL,
    TeamId			INT unsigned NOT NULL,
    PPG				DECIMAL(5,3) NULL,
    RPG				DECIMAL(5,3) NULL,
    APG				DECIMAL(5,3) NULL,
    BPG				DECIMAL(5,3) NULL,
    SPG				DECIMAL(5,3) NULL,
    FG				DECIMAL(5,3) NULL,
    FG3				DECIMAL(5,3) NULL,
    FT				DECIMAL(5,3) NULL,
    CONSTRAINT
    FOREIGN KEY (PlayerId)
		REFERENCES PLAYER(PlayerId)
        ON DELETE CASCADE,	
	FOREIGN KEY (TeamId)
		REFERENCES TEAM(TeamId)
        ON DELETE CASCADE
);

DROP TABLE IF EXISTS AWARD;
CREATE TABLE AWARD
(
	AwardId			INT unsigned,
    Name			VARCHAR(40),
    Description 	VARCHAR(100)
);

DROP TABLE IF EXISTS AWARD_WINNER;
CREATE TABLE AWARD_WINNER
(
	PlayerId		INT unsigned,
    AwardId			INT unsigned,
    YearWon			YEAR,
    CONSTRAINT
		PRIMARY KEY (AwardId, YearWon)
);

